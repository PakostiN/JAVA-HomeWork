import java.util.Scanner;

public class Zad10 {

	public static void main(String[] args) {
		System.out.println("Напишете програма, която приема от конзолата 2 числа a и b (b > a) и изкарва на екрана всички числа от a до b, които се делят на 3 без остатък");
		Scanner test = new Scanner(System.in);
		System.out.println("Въведете първото число: ");

		int a = test.nextInt();

		System.out.println("Въведете второто число: ");

		int b = test.nextInt();
		if (a > b) {
			System.out.println("4ao");
		} else {
			for (int i = a; i < b; i++) {
				if (i < b && i % 3 == 0) {
					System.out.println(i);
				}

			}
		}
		test.close();
	}
}